import random  # для случайных чисел

# компьютер загадывает число
number = str(random.randint(1000, 9999))  # число от 1000 до 9999
print("Число из 4 цифр. Угадай!")

# пока человек не угадает, играем
while True:
    num1 = input("Введи своё число из 4 цифр: ")  # спрашиваем у человека
    
    # проверяем, что ввели 4 цифры
    if len(num1) != 4:
        print("Надо 4 цифры, попробуй ещё раз!")

    
    # считаем быков и коров
    bik = 0  # быки
    corova = 0   # коровы
    
    # проверяем каждую цифру
    for igra in range(4):  # смотрим позиции 0, 1, 2, 3
        if num1[igra] == number[igra]:  # если цифра на своём месте
            bik = bik + 1      # прибавляем быка
        else:  # если не на своём месте, проверяем, есть ли она вообще
            if num1[igra] in number:  # если цифра есть в числе
                corova = corova + 1     # прибавляем корову
    
    # если угадал все быков (4), то победа
    if bik == 4:
        print("угадал! Число было:", number)
        break  # выходим из цикла
    
    # показываем результат попытки
    print(bik, "б,", corova, "к")  # выводим быков и коров