# 1. Есть три кортежа целых чисел необходимо найти
# элементы, которые есть во всех кортежах.
# Исходные кортежи
t1 = (1, 2, 3, 4, 5)
t2 = (3, 4, 5, 6, 7)
t3 = (5, 6, 7, 8, 9)

# Преобразуем кортежи в множества
set1 = set(t1)
set2 = set(t2)
set3 = set(t3)

# Находим пересечение всех трёх множеств
elem = set1 & set2 & set3 

# Выводим результат
print("Элементы, которые есть во всех кортежах:", elem)