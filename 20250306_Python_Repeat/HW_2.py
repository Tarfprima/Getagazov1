# 2. Есть три кортежа целых чисел необходимо найти
# элементы, которые уникальны для каждого списка.

# Задаём три кортежа целых чисел
tuple1 = (1, 2, 3, 4, 5)
tuple2 = (2, 4, 6, 8, 10)
tuple3 = (3, 5, 7, 9, 11)

# Преобразуем кортежи в множества, чтобы работать с операциями
set1 = set(tuple1)  # Множество из первого кортежа
set2 = set(tuple2)  # Множество из второго кортежа
set3 = set(tuple3)  # Множество из третьего кортежа

# Находим элементы, уникальные для каждого множества
# Уникальные для set1: те, что есть в set1, но нет в set2 и set3
unique1 = set1 - (set2 | set3)  # Вычитаем объединение set2 и set3 из set1

# Уникальные для set2: те, что есть в set2, но нет в set1 и set3
unique2 = set2 - (set1 | set3)  # Вычитаем объединение set1 и set3 из set2

# Уникальные для set3: те, что есть в set3, но нет в set1 и set2
unique3 = set3 - (set1 | set2)  # Вычитаем объединение set1 и set2 из set3

# Выводим результаты
print('Уникальные элементы первого кортежа:', unique1)
print('Уникальные элементы второго кортежа:', unique2)
print('Уникальные элементы третьего кортежа:', unique3)

# Объединяем все уникальные элементы в одно множество
all_unique = unique1 | unique2 | unique3
print('Все уникальные элементы:', all_unique)

# '|' — объединение.
# '-' — разность (удаляет элементы, которые есть в другом множестве).