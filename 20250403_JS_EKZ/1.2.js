// Запускаем код при загрузке страницы
window.addEventListener('load', function() {
    // Задаем три числа
    let num1 = 6;
    let num2 = -2;
    let num3 = 3.5;
    // Сравниваем числа и находим наименьшее
    let min = num1; // Сначала считаем, что первое число минимальное
    if (num2 < min) {
        min = num2; // Если второе меньше, обновляем минимум
    }
    if (num3 < min) {
        min = num3; // Если третье меньше, обновляем минимум
    }
    // Выводим наименьшее число в консоль
    console.log(min);
});