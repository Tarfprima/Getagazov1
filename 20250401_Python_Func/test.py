# Задача: 
# 1. Написать функцию, которая вычесляет площадь квартиры. 
# Квартира - массив пар 'Ширина, длина' для каждой комнаты. 
# Сохранить результат в файл. 

# Функция для подсчёта площади квартиры
def kvartira(komnata):
    nums = 0  # общая площадь 0
    
    # проходим по каждой комнате
    for komnati in komnata:  
        width = komnati[0]  # ширина 
        length = komnati[1]  # длина 
        total = width * length  # площадь комнаты
        nums = nums + total  # прибавляем к общей
    
    # сохраняем результат в файл
    num2 = open('hata.txt', 'w') # запись
    num2.write("Площадь квартиры: " + str(nums))  # результат str преобразует в строку nums
    num2.close()  
    
    return nums  # возвращаем площадь

# Проверяем
num1 = [(1, 2), (8, 9), (3, 4), (10, 12)]  
result = kvartira(num1)
print("Общая площадь:", result)

